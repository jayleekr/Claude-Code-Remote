/**
 * Telegram Webhook Handler
 * Handles incoming Telegram messages and commands
 */

const express = require('express');
const axios = require('axios');
const Logger = require('../../core/logger');

class TelegramWebhookHandler {
    constructor(config = {}) {
        this.config = config;
        this.logger = new Logger('TelegramWebhook');
        this.sessionManager = config.sessionManager; // Injected from hub
        this.commandExecutor = config.commandExecutor; // Injected from hub
        this.app = express();
        this.apiBaseUrl = 'https://api.telegram.org';
        this.botUsername = null; // Cache for bot username

        this._setupMiddleware();
        this._setupRoutes();
    }

    _setupMiddleware() {
        // Parse JSON for all requests
        this.app.use(express.json());
    }

    _setupRoutes() {
        // Telegram webhook endpoint
        this.app.post('/webhook/telegram', this._handleWebhook.bind(this));

        // Health check endpoint
        this.app.get('/health', (req, res) => {
            res.json({ status: 'ok', service: 'telegram-webhook' });
        });
    }

    /**
     * Generate network options for axios requests
     * @returns {Object} Network options object
     */
    _getNetworkOptions() {
        const options = {};
        if (this.config.forceIPv4) {
            options.family = 4;
        }
        return options;
    }

    async _handleWebhook(req, res) {
        try {
            const update = req.body;
            
            // Handle different update types
            if (update.message) {
                await this._handleMessage(update.message);
            } else if (update.callback_query) {
                await this._handleCallbackQuery(update.callback_query);
            }
            
            res.status(200).send('OK');
        } catch (error) {
            this.logger.error('Webhook handling error:', error.message);
            res.status(500).send('Internal Server Error');
        }
    }

    async _handleMessage(message) {
        const chatId = message.chat.id;
        const userId = message.from.id;
        const messageText = message.text?.trim();
        
        if (!messageText) return;

        // Check if user is authorized
        if (!this._isAuthorized(userId, chatId)) {
            this.logger.warn(`Unauthorized user/chat: ${userId}/${chatId}`);
            await this._sendMessage(chatId, '‚ö†Ô∏è You are not authorized to use this bot.');
            return;
        }

        // Handle /start command
        if (messageText === '/start') {
            await this._sendWelcomeMessage(chatId);
            return;
        }

        // Handle /help command
        if (messageText === '/help') {
            await this._sendHelpMessage(chatId);
            return;
        }

        // Parse command - support server:number format
        // /cmd kr4:1 <command> or /cmd local:1 <command> or /cmd ABC12345 <command>
        const commandMatch = messageText.match(/^\/cmd\s+([a-z0-9]+:\d+|[A-Z0-9]{8})\s+(.+)$/i);
        if (!commandMatch) {
            await this._sendMessage(chatId,
                '‚ùå Invalid format. Use:\n`/cmd <server>:<number> <command>`\n\nExample:\n`/cmd kr4:1 analyze this code`\n`/cmd local:1 show me the code`',
                { parse_mode: 'Markdown' });
            return;
        }

        const identifier = commandMatch[1]; // server:number or token
        const command = commandMatch[2];

        await this._processCommand(chatId, identifier, command);
    }

    async _processCommand(chatId, identifier, command) {
        // Find session via SessionManager
        const session = await this.sessionManager.findSession(identifier);
        if (!session) {
            await this._sendMessage(chatId,
                '‚ùå Invalid or expired session. Please wait for a new task notification.',
                { parse_mode: 'Markdown' });
            return;
        }

        // Check if session is expired (expiresAt is in seconds, Date.now() is in milliseconds)
        if (session.expiresAt * 1000 < Date.now()) {
            await this._sendMessage(chatId,
                '‚ùå Session has expired. Please wait for a new task notification.',
                { parse_mode: 'Markdown' });
            return;
        }

        try {
            // Execute command via CommandExecutor
            const tmuxSession = session.tmuxSession || 'default';
            await this.commandExecutor.execute(session.serverId, command, tmuxSession);

            // Send confirmation
            await this._sendMessage(chatId,
                `‚úÖ *Command sent to [${session.serverId.toUpperCase()}]*\n\nüìù *Command:* ${command}\nüñ•Ô∏è *Session:* ${tmuxSession}\nüéØ *Target:* ${identifier}\n\nClaude is now processing your request...`,
                { parse_mode: 'Markdown' });

            // Log command execution
            this.logger.info(`Command executed - User: ${chatId}, Session: ${identifier}, Server: ${session.serverId}, Command: ${command}`);

        } catch (error) {
            this.logger.error('Command execution failed:', error.message);
            await this._sendMessage(chatId,
                `‚ùå *Command execution failed:* ${error.message}`,
                { parse_mode: 'Markdown' });
        }
    }

    async _handleCallbackQuery(callbackQuery) {
        const chatId = callbackQuery.message.chat.id;
        const data = callbackQuery.data;
        
        // Answer callback query to remove loading state
        await this._answerCallbackQuery(callbackQuery.id);
        
        if (data.startsWith('personal:')) {
            const sessionNumber = data.split(':')[1];
            // Send personal chat command format
            await this._sendMessage(chatId,
                `üìù *Personal Chat Command Format:*\n\n\`/cmd ${sessionNumber} <your command>\`\n\n*Example:*\n\`/cmd ${sessionNumber} show me the latest changes\`\n\nüí° *Just type the number and your command!*`,
                { parse_mode: 'Markdown' });
        } else if (data.startsWith('group:')) {
            const sessionNumber = data.split(':')[1];
            // Send group chat command format with @bot_name
            const botUsername = await this._getBotUsername();
            await this._sendMessage(chatId,
                `üë• *Group Chat Command Format:*\n\n\`@${botUsername} /cmd ${sessionNumber} <your command>\`\n\n*Example:*\n\`@${botUsername} /cmd ${sessionNumber} show me the latest changes\`\n\nüí° *Just type the number and your command!*`,
                { parse_mode: 'Markdown' });
        } else if (data.startsWith('session:')) {
            const sessionNumber = data.split(':')[1];
            // For backward compatibility - send help message for old callback buttons
            await this._sendMessage(chatId,
                `üìù *How to send a command:*\n\nType:\n\`/cmd ${sessionNumber} <your command>\`\n\nExample:\n\`/cmd ${sessionNumber} show me the latest changes\`\n\nüí° *Just type the number and your command!*`,
                { parse_mode: 'Markdown' });
        }
    }

    async _sendWelcomeMessage(chatId) {
        const message = `ü§ñ *Welcome to Claude Code Remote Bot!*\n\n` +
            `I'll notify you when Claude completes tasks across multiple servers.\n\n` +
            `When you receive a notification, send commands using server:number format:\n` +
            `\`/cmd kr4:1 <your command>\`\n\n` +
            `Type /help for more information.`;

        await this._sendMessage(chatId, message, { parse_mode: 'Markdown' });
    }

    async _sendHelpMessage(chatId) {
        const message = `üìö *Claude Code Remote Bot Help*\n\n` +
            `*Commands:*\n` +
            `‚Ä¢ \`/start\` - Welcome message\n` +
            `‚Ä¢ \`/help\` - Show this help\n` +
            `‚Ä¢ \`/cmd <server>:<number> <command>\` - Send command to Claude\n\n` +
            `*Examples:*\n` +
            `\`/cmd kr4:1 analyze this code\`\n` +
            `\`/cmd local:1 show me the latest changes\`\n\n` +
            `*Multi-Server Support:*\n` +
            `‚Ä¢ Each server has its own session numbering (kr4:1, local:1, etc.)\n` +
            `‚Ä¢ Notifications show which server they came from: [KR4], [LOCAL]\n` +
            `‚Ä¢ Sessions expire after 24 hours\n\n` +
            `*Server Format:*\n` +
            `‚Ä¢ \`kr4:1\` - Session #1 on builder-kr-4 server\n` +
            `‚Ä¢ \`local:1\` - Session #1 on local machine\n` +
            `‚Ä¢ \`aws1:1\` - Session #1 on AWS server (if configured)`;

        await this._sendMessage(chatId, message, { parse_mode: 'Markdown' });
    }

    _isAuthorized(userId, chatId) {
        // Check whitelist
        const whitelist = this.config.whitelist || [];
        
        if (whitelist.includes(String(chatId)) || whitelist.includes(String(userId))) {
            return true;
        }
        
        // If no whitelist configured, allow configured chat/user
        if (whitelist.length === 0) {
            const configuredChatId = this.config.chatId || this.config.groupId;
            if (configuredChatId && String(chatId) === String(configuredChatId)) {
                return true;
            }
        }
        
        return false;
    }

    async _getBotUsername() {
        if (this.botUsername) {
            return this.botUsername;
        }

        try {
            const response = await axios.get(
                `${this.apiBaseUrl}/bot${this.config.botToken}/getMe`,
                this._getNetworkOptions()
            );
            
            if (response.data.ok && response.data.result.username) {
                this.botUsername = response.data.result.username;
                return this.botUsername;
            }
        } catch (error) {
            this.logger.error('Failed to get bot username:', error.message);
        }
        
        // Fallback to configured username or default
        return this.config.botUsername || 'claude_remote_bot';
    }

    async _sendMessage(chatId, text, options = {}) {
        try {
            await axios.post(
                `${this.apiBaseUrl}/bot${this.config.botToken}/sendMessage`,
                {
                    chat_id: chatId,
                    text: text,
                    ...options
                },
                this._getNetworkOptions()
            );
        } catch (error) {
            this.logger.error('Failed to send message:', error.response?.data || error.message);
        }
    }

    async _answerCallbackQuery(callbackQueryId, text = '') {
        try {
            await axios.post(
                `${this.apiBaseUrl}/bot${this.config.botToken}/answerCallbackQuery`,
                {
                    callback_query_id: callbackQueryId,
                    text: text
                },
                this._getNetworkOptions()
            );
        } catch (error) {
            this.logger.error('Failed to answer callback query:', error.response?.data || error.message);
        }
    }

    async setWebhook(webhookUrl) {
        try {
            const response = await axios.post(
                `${this.apiBaseUrl}/bot${this.config.botToken}/setWebhook`,
                {
                    url: webhookUrl,
                    allowed_updates: ['message', 'callback_query']
                },
                this._getNetworkOptions()
            );

            this.logger.info('Webhook set successfully:', response.data);
            return response.data;
        } catch (error) {
            this.logger.error('Failed to set webhook:', error.response?.data || error.message);
            throw error;
        }
    }

    start(port = 3000) {
        this.app.listen(port, () => {
            this.logger.info(`Telegram webhook server started on port ${port}`);
        });
    }
}

module.exports = TelegramWebhookHandler;
